  props: {
    banners: {
      type: Array,
      required: true,
    },
  },
  setup() {
    onMounted(() => {
      const $ = window.$
      if ($ && $('.banner-slider').slick) {
        $('.banner-slider').slick({
          arrows: true,
          prevArrow: $('#banner-prev'),
          nextArrow: $('#banner-next'),
